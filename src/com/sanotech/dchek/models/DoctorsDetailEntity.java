package com.sanotech.dchek.models;


public class DoctorsDetailEntity {
	public String drFname;
	public String drLname;
	public String drHospital;
	public int drId;
	public String drPhoNo;
	public String drEmail;
	public String drLastVisit;
	public String drNextVisit;
	
	
	public String getDrFname() {
		return drFname;
	}
	public void setDrFname(String drFname) {
		this.drFname = drFname;
	}
	public String getDrLname() {
		return drLname;
	}
	public void setDrLname(String drLname) {
		this.drLname = drLname;
	}
	public String getDrHospital() {
		return drHospital;
	}
	public void setDrHospital(String drHospital) {
		this.drHospital = drHospital;
	}
	public int getDrId() {
		return drId;
	}
	public void setDrId(int drId) {
		this.drId = drId;
	}
	public String getDrPhoNo() {
		return drPhoNo;
	}
	public void setDrPhoNo(String drPhoNo) {
		this.drPhoNo = drPhoNo;
	}
	public String getDrEmail() {
		return drEmail;
	}
	public void setDrEmail(String drEmail) {
		this.drEmail = drEmail;
	}
	public String getDrLastVisit() {
		return drLastVisit;
	}
	public void setDrLastVisit(String drLastVisit) {
		this.drLastVisit = drLastVisit;
	}
	public String getDrNextVisit() {
		return drNextVisit;
	}
	public void setDrNextVisit(String drNextVisit) {
		this.drNextVisit = drNextVisit;
	}
	
}
